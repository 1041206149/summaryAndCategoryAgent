"""
摘要提示词模板
"""


class SummaryPrompts:
    """摘要提示词管理"""

    TEMPLATE = """作为一名专业的对话分析师，请分析以下客服对话记录，提取关键信息并按照以下格式输出结构化摘要:

【沟通内容】
提取用户反馈的主要问题和诉求要点，保持简洁明了。如涉及产品需明确指出(如飞享会员)。多个诉求分条呈现。

【方案详情】
列出针对每个诉求的具体解决方案；如果有如下信息则需要注明，没有则忽略（仅给出解决方案）:
- 若涉及金额要明确标注具体数字
- 如果有减免、退款等操作要清晰说明
- 如果有订单编号则要说明

【处理结果】
说明最终处理状态，包括:
- 用户是否接受方案
- 相关操作是否已完成
- 如有待跟进事项需注明（没有则忽略这条）

要求:
1. 保持客观中立的叙述语气
2. 方案和金额必须准确对应原文
3. 按照【】分类标题组织内容
4. 多个问题按时间顺序分别完整描述
5. 每个部分表述要简明扼要
6. 总字数不要超过120字

请基于以上要求，分析如下对话内容:
{conversation}"""

    @classmethod
    def create_prompt(cls, conversation: str) -> str:
        """创建摘要提示词"""
        return cls.TEMPLATE.format(conversation=conversation)
